public class Operations {
    void addition(int a, int b) {
        System.out.println("Result is: " + (a + b) + "\n");
    }

    void subtraction(int a, int b) {
        System.out.println("Result is: " + (a - b) + "\n");
    }

    void multiplication(int a, int b) {
        System.out.println("Result is: " + (a * b) + "\n");
    }

    void division(int a, int b) {
        System.out.println("Result is: " + (a / b) + "\n");
    }
}
